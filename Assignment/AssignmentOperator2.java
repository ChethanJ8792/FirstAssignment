package Assignment;

public class AssignmentOperator2 {

	public static void main(String[] args)
	{
		int num=43;
		System.out.println(num);
		num+=43;
		System.out.println(num);
		num-=43;
		System.out.println(num);
		num*=43;
		System.out.println(num);
		num/=43;
		System.out.println(num);
		num%=43;
		System.out.println(num);
	}

	}

