package Assignment;

public class BitwiseOperator {

	public static void main(String[] args) 
	{
		int one=12;
		int two=14;
		System.out.println(one&two);
		System.out.println(one|two);
		System.out.println(one^two);
		System.out.println(one=~two);
		System.out.println(one<<3);
		System.out.println(one>>4);

	}

}
