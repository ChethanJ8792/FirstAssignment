package Assignment;

public class DecrementStatic {

	public static void main(String[] args) 
	{
		byte it=10;
		short that=20;
		int give=30;
		long jump=40;
		System.out.println(--it);
		System.out.println(--that);
		System.out.println(--give);
		System.out.println(--jump);
		System.out.println(it--);
		System.out.println(that--);
		System.out.println(give--);
		System.out.println(jump--);
		System.out.println(--it);
		System.out.println(--that);
		System.out.println(--give);
		System.out.println(--jump);
		
	}

}
