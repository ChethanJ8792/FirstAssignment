package Static;

public class StaticVariables
{
	static int temp=25;						//initializing declaring integer and string
	static String device="Air cooler of brand O general";
	

	public static void main(String[] args)
	{
		System.out.println("temp: "+temp);			//since they are static no object needed
		System.out.println("device: "+device);		//printing output

	}

}
